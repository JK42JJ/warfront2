def stage_for(streak: int) -> int:
    """연속 훈련일 → 캐릭터 단계 (5일마다 +1, 최종 9단계 유지)."""
    return min(max(streak, 0) // 5, len(STAGES) - 1)


def stage_info(stage: int) -> tuple[str, str]:
    name, lore, _ = STAGES[min(stage, len(STAGES) - 1)]
    return name, lore


def idle_frame(stage: int, tick: int) -> str:
    _, _, frames = STAGES[min(stage, len(STAGES) - 1)]
    return frames[tick % len(frames)]


def days_to_next(streak: int) -> int | None:
    """다음 진화까지 남은 연속일 (최종 단계면 None)."""
    if stage_for(streak) >= len(STAGES) - 1:
        return None
    return 5 - (streak % 5)


def evolution_frames(new_stage: int) -> list[str]:
    """진화 연출 프레임 시퀀스: 반짝임 3장 → 새 모습."""
